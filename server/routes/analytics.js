const express = require("express");
const router = express.Router();
const analyticsController = require("../controllers/analyticsController");

// GET request to get total sales report
router.get("/total-sales", analyticsController.getTotalSalesReport);

// GET request to get total revenue report
router.get("/total-revenue", analyticsController.getTotalRevenueReport);

// GET request to get average order value report
router.get(
  "/average-order-value",
  analyticsController.getAverageOrderValueReport
);

// GET request to get most ordered items report
router.get(
  "/most-ordered-items",
  analyticsController.getMostOrderedItemsReport
);

// GET request to get most active customers report
router.get(
  "/most-active-customers",
  analyticsController.getMostActiveCustomersReport
);

module.exports = router;
